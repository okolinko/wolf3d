#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		x;
	size_t	i;

	x = 0;
	i = 0;
	while (s1[x])
		x++;
	while (s2[i] && (i < n))
	{
		s1[x] = s2[i];
		x++;
		i++;
	}
	s1[x] = '\0';
	return (s1);
}

#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int x;
	int i;

	x = 0;
	i = 0;
	while (s1[x] != '\0')
		x++;
	while (s2[i] != '\0')
	{
		s1[x] = s2[i];
		x++;
		i++;
	}
	s1[x] = '\0';
	return (s1);
}
